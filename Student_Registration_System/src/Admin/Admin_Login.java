    private void lblForgotPasswordMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_lblForgotPasswordMouseClicked
    {//GEN-HEADEREND:event_lblForgotPasswordMouseClicked
        // TODO add your handling code here:
        String id = JOptionPane.showInputDialog("Enter your Id number :");

        try
        {
            int userid = Integer.parseInt(id);
            if (id == null || id.length() == 0)
            {
                throw new Exception();
            }

            String query = "SELECT * FROM admin_credentials WHERE adminId =?";
            try
            {
              
                Connection con = CommonFunctions.Functions.getConnection();
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, id);

                ResultSet rst = pst.executeQuery();
                if (rst.next())
                {
                    JOptionPane.showMessageDialog(this, "Your Password is : " + rst.getString("admin_password"));
                    clearText();
                    lblForgotPassword.setVisible(false);

                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Sorry , User not found. Please check your Id.", "Forgot Password", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }

        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Please Enter valid Id number", "Forgot Password", JOptionPane.ERROR_MESSAGE);
            evt.consume();
        }


    }//GEN-LAST:event_lblForgotPasswordMouseClicked

