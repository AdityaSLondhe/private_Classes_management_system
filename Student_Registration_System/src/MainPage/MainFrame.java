    private void btn_update_upActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_update_upActionPerformed
    {//GEN-HEADEREND:event_btn_update_upActionPerformed
        // TODO add your handling code here:
        
        Date start = date_start_up.getDate();
       
        SimpleDateFormat de = new SimpleDateFormat("YYYY-MM-dd");
        // enq date
        String strt_= de.format(start);
        
        Date end_d = date_end_up.getDate();
        
        String end = de.format(end_d);
        String batch = txt_batch_up.getText();
        
        int total_std = Integer.parseInt(txt_total_students_up.getText());
        
        int syll         = Integer.parseInt(txt_syllabus_up.getText());
        
        int holidays =Integer.parseInt( txt_holidays_up.getText());
        
        int lectures = Integer.parseInt(txt_lectures_up.getText());
        
        
        
        
        
        try
        {
            Connection con =   CommonFunctions.Functions.getConnection();
            
            String q = " update courseupdates set total_students = ? ,syllabus_completes =? ,expected_end_date = ?,holidays_given =?,total_lectures= ? where course_id = ? and batch =?";
                   
            PreparedStatement pst = con.prepareStatement(q);
            
            pst.setInt(1, total_std);
            pst.setInt(2, syll);
            pst.setString(3, end);
            pst.setInt(4,holidays);
            pst.setInt(5, lectures);
            pst.setInt(6,combo_course_up.getSelectedIndex()+1);
            pst.setString(7, batch);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Updated successfully");
            
        }
        catch (Exception e)
        {
            System.out.println("e = "+e);
        }
               
                

        
        
        
    }//GEN-LAST:event_btn_update_upActionPerformed

    private void btn_search_upActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btn_search_upActionPerformed
    {//GEN-HEADEREND:event_btn_search_upActionPerformed
        // TODO add your handling code here:
        
          String batch = txt_batch_up.getText();
          
          int course_id = combo_course_up.getSelectedIndex()+1;
           if(course_id==0)
          {
              JOptionPane.showMessageDialog(this, "Please select course ");
              return;
          }
          if(batch.length()==0)
          {
              JOptionPane.showMessageDialog(this, "Please enter batch year");
              return;
          }
          
             try
        {
            Connection con = CommonFunctions.Functions.getConnection();
            
            String q =  " select * from courseupdates inner join course on course.course_id = courseupdates.course_id where course.course_id=? AND courseupdates.batch =?";
            
            PreparedStatement pst = con.prepareStatement(q);
            
            pst.setInt(1,course_id);
            pst.setString(2, batch);
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next())
            {
                txt_batch_up_txt.setText(rs.getString("batch"));
                txt_holidays_up.setText(rs.getString("holidays_given"));
                txt_lectures_up.setText(rs.getInt("total_lectures")+"");
                txt_syllabus_up.setText(rs.getInt("syllabus_completes")+"");
                txt_total_students_up.setText(rs.getInt("total_students")+"");
                txt_course_name.setText(rs.getString("coursename"));
                date_end_up.setDate(rs.getDate("expected_end_date"));
                date_start_up.setDate(rs.getDate("start_date")); 
                
                btn_update_up.setEnabled(true);
            }
            
            
           }
        catch (Exception e)
        {
            System.out.println("e =  "+e);
        }
        
        
        
    }//GEN-LAST:event_btn_search_upActionPerformed

