    private void setComboCourses()
    {
        bcomboLanguages.removeAllItems();
        combo_course_up.removeAllItems();
        try
        {
            Connection con = CommonFunctions.Functions.getConnection();
            String q = "SELECT * FROM course order by course_id";
            PreparedStatement st = con.prepareStatement(q);

            ResultSet rs = st.executeQuery();

            while (rs.next())
            {
                bcomboLanguages.addItem(rs.getString("coursename"));
                combo_course_up.addItem(rs.getString("coursename"));
               
            }
        }
        
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e);

        }
        
    }
    
    
