    private void btnadminLoginActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnadminLoginActionPerformed
    {//GEN-HEADEREND:event_btnadminLoginActionPerformed
        // TODO add your handling code here:
        String name = txtadminLoginUsername.getText();
        String password = new String(txtadminLoginPassword.getPassword());
        try
        {
            Connection con = CommonFunctions.Functions.getConnection();
            String query = "SELECT * FROM admin_credentials WHERE admin_name=? AND admin_password=?";

            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setString(2, password);
            ResultSet res;
            res = pst.executeQuery();
            if (res.next())
            {
                JOptionPane.showMessageDialog(this, "Login Successfull");
                
                clearText();
                FirstPage.main(null);
                this.dispose();

            }
            else
            {
                clearText();
                JOptionPane.showMessageDialog(this, "Please enter valid credentials");
                lblForgotPassword.setVisible(true);

            }

        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
            lblForgotPassword.setVisible(true);

        }


    }//GEN-LAST:event_btnadminLoginActionPerformed

